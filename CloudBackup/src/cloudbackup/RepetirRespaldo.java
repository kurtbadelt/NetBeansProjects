             Ziper ziper = new Ziper();
             ziper.zipMultipleFiles(nombreArchivo, Archivos);
             
             String label = nombreArchivo+";"+System.getProperty("user.name")+";"+util.TimeStamp()+";";
             String savedconfig = label;
